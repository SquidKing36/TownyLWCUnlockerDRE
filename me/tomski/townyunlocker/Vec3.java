package me.tomski.townyunlocker;

public class Vec3
{
    private int x;
    private int y;
    private int z;
    
    public Vec3(final int x, final int z) {
        super();
        this.x = x;
        this.z = z;
    }
    
    public int getX() {
        return this.x;
    }
    
    public int getY() {
        return this.y;
    }
    
    public int getZ() {
        return this.z;
    }
}
